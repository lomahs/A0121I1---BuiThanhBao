function changeImgA() {
  let imgA = document.getElementById("imgA");

  if (imgA.src == "http://127.0.0.1:5500/Game%20puzzle/Images/Hinh1a.png") {
    imgA.src = "Images/Hinh2a.png";
  } else if (
    imgA.src == "http://127.0.0.1:5500/Game%20puzzle/Images/Hinh2a.png"
  ) {
    imgA.src = "Images/Hinh3a.png";
  } else imgA.src = "Images/Hinh1a.png";
}

